import Foundation

class MathematicFunctions {
    
    func addIntegers(x : Int, y: Int) -> Int{
        return x + y
    }
    
    func minusIntegers(x : Int, y:Int) -> Int{
        return x - y
    }
    
    func multiplNumbers(x : Int, y: Int) -> Int{
        return x * y
    }
    
    func divideIntegers(x : Int, y: Int) -> Int{
        return x / y
    }
    
    
    
}
